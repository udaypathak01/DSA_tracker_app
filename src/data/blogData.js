/**
 * Get blog post by slug
 */
export const getBlogBySlug = (slug) => {
  return blogPosts.find(post => post.slug === slug);
};

/**
 * Get related blog posts
 */
export const getRelatedPosts = (postId, limit = 2) => {
  const currentPost = blogPosts.find(post => post.id === postId);
  if (!currentPost || !currentPost.relatedPosts) return [];

  return currentPost.relatedPosts
    .map(id => blogPosts.find(post => post.id === id))
    .filter(Boolean)
    .slice(0, limit);
};

/**
 * Get all blog posts sorted by date
 */
export const getAllBlogs = () => {
  return [...blogPosts].sort((a, b) =>
    new Date(b.publishDate) - new Date(a.publishDate)
  );
};
