/**
 * Get all unique topics from resources
 */
export const getAllTopics = () => {
  const topics = new Set();
  resources.forEach((resource) => {
    resource.topic.forEach((t) => topics.add(t));
  });
  return Array.from(topics).sort();
};

/**
 * Get all resource types
 */
export const getResourceTypes = () => {
  const types = new Set();
  resources.forEach((resource) => {
    types.add(resource.type);
  });
  return Array.from(types).sort();
};

/**
 * Get all categories in desired order
 * Order: YouTube Channels, Roadmaps, Interview Preparation, Websites
 */
export const getCategories = () => {
  const categoryOrder = [
    'YouTube Channels',
    'Roadmaps',
    'Interview Preparation',
    'Websites',
  ];
  const categories = new Set();
  resources.forEach((resource) => {
    categories.add(resource.category);
  });
  // Return only existing categories in the specified order
  return categoryOrder.filter((cat) => categories.has(cat));
};
